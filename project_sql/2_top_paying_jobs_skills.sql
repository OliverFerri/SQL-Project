/*
to search for general data anaylst, change salary average from hour to year
and remove job_title condition and change to job_tite_short = 'Data Analyst'
*/

WITH top_jobs AS (
    SELECT
        DENSE_RANK() OVER (ORDER BY salary_year_avg DESC) AS rnk,
        job_id,
        job_title,
        salary_year_avg,
        name AS company_name
    FROM job_postings_fact j
    JOIN company_dim c ON j.company_id = c.company_id
    WHERE 
        job_title_short = 'Data Analyst'
        AND salary_year_avg IS NOT NULL
        AND job_location = 'Anywhere'
)

SELECT 
    t.job_id,
    job_title,
    company_name,
    salary_year_avg,
    skills
FROM top_jobs t
JOIN skills_job_dim sj ON t.job_id = sj.job_id
JOIN skills_dim s ON sj.skill_id = s.skill_id
WHERE rnk <= 12;

